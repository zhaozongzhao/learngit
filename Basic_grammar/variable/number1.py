import math
#数学函数
print(abs(-10))
print(math.ceil(4.2))   #返回数字的上入整数
print(math.fabs(-10.0))   #获取绝对值
print(math.floor(4.3))  #获取下入整数
print(math.log(100,10)) #获取自然对数
print(max(1,2))
print(min(1,2))
print(math.modf(100.01)) # 返回数值的小数部分和整数部分

print(math.pow(2,2))     #计算2的2次方
print(math.pow(3,3))   #计算3的3次方对3取模
print(round(4.9876,2))   #返回浮点数四舍五入的值,精确到小数点后两位
print(math.sqrt(10))     #获取数据的平方根